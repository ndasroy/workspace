
public class SwitchCase 
{

	public static void main(String[] args) 
	{
		int S=2;
		switch (S) 
		{
		  case 1:
		        System.out.println("Hi");
		        break;
		  case 2: 
			  	System.out.println("Hello");
		        break;
		  case 3:
			  	System.out.println("How r u ?");
			  	break;
		  default:
			  System.out.println("Done");
			  break;
		}
	}
}
